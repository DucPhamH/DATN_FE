import { useState } from 'react'
import { motion } from 'framer-motion'
import { IoIosArrowDown } from 'react-icons/io'
import { NavLink, useLocation } from 'react-router-dom'

export default function Submenu({ data }) {
  const { pathname } = useLocation()
  // console.log(pathname)
  const [subMenuOpen, setSubMenuOpen] = useState(false)
  return (
    <>
      <li
        className={`link ${pathname.includes(data.name) && 'text-blue-600'}`}
        onClick={() => setSubMenuOpen(!subMenuOpen)}
      >
        <data.icon size={23} className='min-w-max' />
        <p className='flex-1'>{data.name}</p>
        <IoIosArrowDown className={` ${subMenuOpen && 'rotate-180'} duration-200 `} />
      </li>
      <motion.ul
        animate={
          subMenuOpen
            ? {
                height: 'fit-content'
              }
            : {
                height: 0
              }
        }
        className='flex h-0 flex-col pl-14 text-[0.8rem] font-normal overflow-hidden'
      >
        {data.menus?.map((menu) => (
          <li key={menu}>
            {/* className="hover:text-blue-600 hover:font-medium" */}
            <NavLink to={`/${data.name}/${menu}`} className='link bg-transparent'>
              {menu}
            </NavLink>
          </li>
        ))}
      </motion.ul>
    </>
  )
}
